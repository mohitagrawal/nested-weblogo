package edu.ufl.cise.bioinformatics.nestedweblogo;

public class LogoBoundry {
	private float leftXPosition;
	private float rightXPosition;
	private float bottomYPosition;
	private float topYPosition;
	public float getLeftXPosition() {
		return leftXPosition;
	}
	public void setLeftXPosition(float leftXPosition) {
		this.leftXPosition = leftXPosition;
	}
	public float getRightXPosition() {
		return rightXPosition;
	}
	public void setRightXPosition(float rightXPosition) {
		this.rightXPosition = rightXPosition;
	}
	public float getBottomYPosition() {
		return bottomYPosition;
	}
	public void setBottomYPosition(float bottomYPosition) {
		this.bottomYPosition = bottomYPosition;
	}
	public float getTopYPosition() {
		return topYPosition;
	}
	public void setTopYPosition(float topYPosition) {
		this.topYPosition = topYPosition;
	}
}
